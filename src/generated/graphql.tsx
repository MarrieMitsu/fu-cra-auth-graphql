import { gql } from '@apollo/client';
import * as Apollo from '@apollo/client';
export type Maybe<T> = T | null;
export type Exact<T extends { [key: string]: unknown }> = { [K in keyof T]: T[K] };
export type MakeOptional<T, K extends keyof T> = Omit<T, K> & { [SubKey in K]?: Maybe<T[SubKey]> };
export type MakeMaybe<T, K extends keyof T> = Omit<T, K> & { [SubKey in K]: Maybe<T[SubKey]> };
/** All built-in and custom scalars, mapped to their actual values */
export type Scalars = {
  ID: string;
  String: string;
  Boolean: boolean;
  Int: number;
  Float: number;
};

export type Query = {
  __typename?: 'Query';
  users?: Maybe<Array<User>>;
  me?: Maybe<User>;
};

export type User = {
  __typename?: 'User';
  id: Scalars['Int'];
  name: Scalars['String'];
  username: Scalars['String'];
  email: Scalars['String'];
  profileImage?: Maybe<Scalars['String']>;
  createdAt: Scalars['String'];
  updatedAt: Scalars['String'];
};

export type Mutation = {
  __typename?: 'Mutation';
  register: UserResponse;
  login: UserResponse;
  logout: Scalars['Boolean'];
  updateUser?: Maybe<User>;
  changePassword?: Maybe<Scalars['Boolean']>;
  deleteUser?: Maybe<DeleteUserResponse>;
};


export type MutationRegisterArgs = {
  input: RegisterInput;
};


export type MutationLoginArgs = {
  input: LoginInput;
};


export type MutationUpdateUserArgs = {
  name: Scalars['String'];
};


export type MutationChangePasswordArgs = {
  password: Scalars['String'];
  id: Scalars['Int'];
};


export type MutationDeleteUserArgs = {
  input: DeleteUserInput;
};

export type UserResponse = {
  __typename?: 'UserResponse';
  errors?: Maybe<Array<FieldError>>;
  user?: Maybe<User>;
  accessToken?: Maybe<Scalars['String']>;
};

export type FieldError = {
  __typename?: 'FieldError';
  field: Scalars['String'];
  message: Scalars['String'];
};

export type RegisterInput = {
  name: Scalars['String'];
  username: Scalars['String'];
  email: Scalars['String'];
  password: Scalars['String'];
};

export type LoginInput = {
  unique: Scalars['String'];
  password: Scalars['String'];
};

export type DeleteUserResponse = {
  __typename?: 'DeleteUserResponse';
  errors?: Maybe<Array<FieldError>>;
  delete: Scalars['Boolean'];
};

export type DeleteUserInput = {
  unique: Scalars['String'];
  verify: Scalars['String'];
  password: Scalars['String'];
};

export type AuthErrorResponseFragment = (
  { __typename?: 'FieldError' }
  & Pick<FieldError, 'field' | 'message'>
);

export type AuthResponseFragment = (
  { __typename?: 'UserResponse' }
  & Pick<UserResponse, 'accessToken'>
  & { errors?: Maybe<Array<(
    { __typename?: 'FieldError' }
    & AuthErrorResponseFragment
  )>>, user?: Maybe<(
    { __typename?: 'User' }
    & UserDataFragment
  )> }
);

export type UserDataFragment = (
  { __typename?: 'User' }
  & Pick<User, 'id' | 'name' | 'username' | 'email' | 'profileImage' | 'createdAt' | 'updatedAt'>
);

export type DeleteUserMutationVariables = Exact<{
  input: DeleteUserInput;
}>;


export type DeleteUserMutation = (
  { __typename?: 'Mutation' }
  & { deleteUser?: Maybe<(
    { __typename?: 'DeleteUserResponse' }
    & Pick<DeleteUserResponse, 'delete'>
    & { errors?: Maybe<Array<(
      { __typename?: 'FieldError' }
      & AuthErrorResponseFragment
    )>> }
  )> }
);

export type LoginMutationVariables = Exact<{
  input: LoginInput;
}>;


export type LoginMutation = (
  { __typename?: 'Mutation' }
  & { login: (
    { __typename?: 'UserResponse' }
    & AuthResponseFragment
  ) }
);

export type LogoutMutationVariables = Exact<{ [key: string]: never; }>;


export type LogoutMutation = (
  { __typename?: 'Mutation' }
  & Pick<Mutation, 'logout'>
);

export type RegisterMutationVariables = Exact<{
  input: RegisterInput;
}>;


export type RegisterMutation = (
  { __typename?: 'Mutation' }
  & { register: (
    { __typename?: 'UserResponse' }
    & AuthResponseFragment
  ) }
);

export type UpdateUserMutationVariables = Exact<{
  name: Scalars['String'];
}>;


export type UpdateUserMutation = (
  { __typename?: 'Mutation' }
  & { updateUser?: Maybe<(
    { __typename?: 'User' }
    & UserDataFragment
  )> }
);

export type MeQueryVariables = Exact<{ [key: string]: never; }>;


export type MeQuery = (
  { __typename?: 'Query' }
  & { me?: Maybe<(
    { __typename?: 'User' }
    & UserDataFragment
  )> }
);

export const AuthErrorResponseFragmentDoc = gql`
    fragment AuthErrorResponse on FieldError {
  field
  message
}
    `;
export const UserDataFragmentDoc = gql`
    fragment UserData on User {
  id
  name
  username
  email
  profileImage
  createdAt
  updatedAt
}
    `;
export const AuthResponseFragmentDoc = gql`
    fragment AuthResponse on UserResponse {
  errors {
    ...AuthErrorResponse
  }
  user {
    ...UserData
  }
  accessToken
}
    ${AuthErrorResponseFragmentDoc}
${UserDataFragmentDoc}`;
export const DeleteUserDocument = gql`
    mutation DeleteUser($input: DeleteUserInput!) {
  deleteUser(input: $input) {
    errors {
      ...AuthErrorResponse
    }
    delete
  }
}
    ${AuthErrorResponseFragmentDoc}`;
